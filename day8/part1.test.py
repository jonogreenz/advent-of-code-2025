from part1 import get_sorted_distances, merge_circuits_if_needed

assert(
    get_sorted_distances(
        [
            (
                162,
                817,
                812,
            ),
            (57, 618, 57),
            (984, 92, 344),
            (425, 690, 689),
        ]
    )
) == [
    (316.90219311326956, (0, 3)),
    (734.8686957545546, (1, 3)),
    (787.814064357828, (0, 1)),
    (888.3186365263311, (2, 3)),
    (1103.7998006885125, (1, 2)),
    (1191.7772442868675, (0, 2)),
]

assert(merge_circuits_if_needed([[0],[1,2,8],[3]], 0, 2)) == [[0,1,2,8],[3]]
